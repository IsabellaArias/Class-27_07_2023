package House;

public class House {
    private String address_house;
    private String owner;
    private int Bedroom_number;

    public String getAddress_house() {
        return address_house;
    }

    public void setAddress_house(String address_house) {
        this.address_house = address_house;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public int getBedroom_number() {
        return Bedroom_number;
    }

    public void setBedroom_number(int bedroom_number) {
        Bedroom_number = bedroom_number;
    }
}
